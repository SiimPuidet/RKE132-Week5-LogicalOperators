//true && true --> true
//true && false --> false
//false && true --> false
//false && false --> false


//OR - || (pipes)

// Vähemalt üks tingimusetest peab olema täidetud ehk vähemalt ühe väljund true.
//true || true --> true
//true || false --> true
//false || true --> true
//false || false --> false

